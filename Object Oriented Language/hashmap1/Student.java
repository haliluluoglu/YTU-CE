package hashmap1;

public class Student {
	private String number;
	private String name;
	
	
	public Student(String number,String name)
	{
		this.number=number;
		this.name=name;
	}

	public String getNumber() {
		return number;
	}


	public String getName() {
		return name;
	}
	
	
	
	

}
